/// Detecta o idioma inicial: pt-BR se o sistema for português do Brasil,
/// senão inglês (fallback pra qualquer outro idioma).
export function detectLang() {
  const langs = [
    ...(navigator.languages || []),
    navigator.language || "",
  ].map((l) => l.toLowerCase());

  // português do Brasil explícito
  if (langs.some((l) => l === "pt-br" || l === "pt_br")) return "pt-BR";
  // português genérico -> trata como pt-BR (melhor que inglês pra lusófono)
  if (langs.some((l) => l.startsWith("pt"))) return "pt-BR";
  // qualquer outro (inclusive en) -> inglês
  return "en";
}
